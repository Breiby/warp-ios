                createView(for: "Gray",
                           colors: [ToriColors.gray50,
                                    ToriColors.gray100,
                                    ToriColors.gray200,
                                    ToriColors.gray300,
                                    ToriColors.gray400,
                                    ToriColors.gray500,
                                    ToriColors.gray600,
                                    ToriColors.gray700,
                                    ToriColors.gray800,
                                    ToriColors.gray900])
            }
            .padding(.horizontal)
        }
    }

    private func createView(for colorGroup: String, colors: [Color]) -> some View {
        return GroupBox(
            content: {
                HStack(spacing: 1) {
                    ForEach(colors, id: \.self) { color in
                        Rectangle()
                            .aspectRatio(1.0, contentMode: .fit)
                            .foregroundColor(color)
                    }
                }
            }, label: {
                Text(colorGroup)
            }
        )
    }
}

