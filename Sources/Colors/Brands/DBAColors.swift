public struct DBAColorsView: View {
    public init() {}

    public var body: some View {
        ScrollView(showsIndicators: false) {
            VStack (alignment: .leading) {
                createView(for: "Jeanblue",
                           colors: [DBAColors.jeanblue50,
                                    DBAColors.jeanblue100,
                                    DBAColors.jeanblue200,
                                    DBAColors.jeanblue300,
                                    DBAColors.jeanblue400,
                                    DBAColors.jeanblue500,
                                    DBAColors.jeanblue600,
                                    DBAColors.jeanblue700,
                                    DBAColors.jeanblue800,
                                    DBAColors.jeanblue900])

                createView(for: "Phthaloblue",
                           colors: [DBAColors.phthaloblue50,
                                    DBAColors.phthaloblue100,
                                    DBAColors.phthaloblue200,
                                    DBAColors.phthaloblue300,
                                    DBAColors.phthaloblue400,
                                    DBAColors.phthaloblue500,
                                    DBAColors.phthaloblue600,
                                    DBAColors.phthaloblue700,
                                    DBAColors.phthaloblue800,
                                    DBAColors.phthaloblue900])

                createView(for: "Green",
                           colors: [DBAColors.green50,
                                    DBAColors.green100,
                                    DBAColors.green200,
                                    DBAColors.green300,
                                    DBAColors.green400,
                                    DBAColors.green500,
                                    DBAColors.green600,
                                    DBAColors.green700,
                                    DBAColors.green800,
                                    DBAColors.green900])

                createView(for: "Yellow",
                           colors: [DBAColors.yellow50,
                                    DBAColors.yellow100,
                                    DBAColors.yellow200,
                                    DBAColors.yellow300,
                                    DBAColors.yellow400,
                                    DBAColors.yellow500,
                                    DBAColors.yellow600,
                                    DBAColors.yellow700,
                                    DBAColors.yellow800,
                                    DBAColors.yellow900])

                createView(for: "Red",
                           colors: [DBAColors.red50,
                                    DBAColors.red100,
                                    DBAColors.red200,
                                    DBAColors.red300,
                                    DBAColors.red400,
                                    DBAColors.red500,
                                    DBAColors.red600,
                                    DBAColors.red700,
                                    DBAColors.red800,
                                    DBAColors.red900])

                createView(for: "Gray",
                           colors: [DBAColors.gray50,
                                    DBAColors.gray100,
                                    DBAColors.gray200,
                                    DBAColors.gray300,
                                    DBAColors.gray400,
                                    DBAColors.gray500,
                                    DBAColors.gray600,
                                    DBAColors.gray700,
                                    DBAColors.gray750,
                                    DBAColors.gray800,
                                    DBAColors.gray850,
                                    DBAColors.gray900,
                                    DBAColors.gray950])
            }
            .padding(.horizontal)
            .navigationTitle("Colors")
            .navigationBarTitleDisplayMode(.inline)
        }
    }

    private func createView(for colorGroup: String, colors: [Color]) -> some View {
        return GroupBox(
            content: {
                HStack(spacing: 1) {
                    ForEach(colors, id: \.self) { color in
                        Rectangle()
                            .aspectRatio(1.0, contentMode: .fit)
                            .foregroundColor(color)
                    }
                }
            }, label: {
                Text(colorGroup)
            }
        )
    }
}

